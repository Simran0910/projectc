<h1><b>Property Crime</b></h1>
<p>Property crimes, or offenses against property, do not necessarily involve the harm of another person. Rather, these crimes involve interference with another person’s right to use or enjoy their own property. </p>
<h3>Property crimes include but are not limited to:</h3>
<p><b>Theft:</b> Larceny refers to a type of theft in which a person takes another person’s property and carries it away, with the intent to permanently deprive the legal owner of their property. Robbery is known as theft by force, and may also be considered a personal crime as it often results in physical and mental harm. Burglary occurs when a person breaks and enters into a home or building, intending to commit a crime. This crime is generally theft, although assault or arson may also constitute burglary.</p>
<p><b>Arson:</b> Arson is the willful and malicious burning or charring of another person’s property or structure;</p>
