<h1><b>Personal Crime</b></h1>
<p>In a personal crime, the victims themselves are directly harmed, whether physically, mentally, or emotionally, by the offender. Personal crimes are the crimes most associated with violence against victims. For example, homicide is the willful killing of a person while murder is the unlawful willful killing of a person.

Homicide can be lawful. A police officer who kills someone using the lawful amount of force in a justified situation has committed a homicide. A lawfully armed homeowner defending him- or herself during a home invasion is also lawful homicide.

Murder, however, is never justified. Murder involves a mens rea, or criminal intent, in which the person planned on killing the victim, either ahead of time, known as 'premeditation', or in the spur of the moment. This 'spur of the moment' murder is sometimes known as 'in the heat of passion'.

Another term for the unlawful killing of an individual is manslaughter. There are many different definitions of manslaughter depending on local statutes of the jurisdiction, but is generally the unintentional killing of a person due to an intentional act of negligence. Think of Sam, a drunk driver who got in an accident and killed someone else. Sam did not intend to kill anyone when he got in the car, but his act of intentional negligence (driving drunk) led to an unintentional and unlawful killing.
Not every personal crime involves the taking of a life.</p>
<h3>Personal crimes are most commonly generalized as a violent crime that causes physical, emotional, or psychological harm to the victim. These crimes are offenses against the person, and can include but are not limited to:</h3>
<p><b>Assault and Battery:</b> Assault refers to the intentional creation of a reasonable apprehension of harm. In other words, assault is a situation in which one person causes another person to fear being harmed. Assault and battery are most commonly considered two distinct personal crimes, although many states do merge the two crimes into the one crime known as “assault and battery.”</p>
<p><b>Battery:</b>It refers to the unauthorized application of force against another person’s body. This results in offensive touching, or actual physical injury. As some jurisdictions define assault as attempted but failed battery, battery charges are commonly grouped with assault to form the single charge of assault and battery.</p>
<p><b>False Imprisonment:</b> False imprisonment refers to one person forcibly restraining another person, against their will, with a risk of being seriously injured or killed. Any person who intentionally restricts another person’s freedom or movement, without their consent, may be liable for false imprisonment.</p>
<p><b>Kidnapping:</b> Kidnapping is defined as the carrying away or confinement of a person by force or deception, without that person’s consent. In other words, kidnapping is the seizure and detention of a person without their consent, while intending to carry away the person at a later time, hold the person for ransom, etc.</p>
<p><b>Homicide:</b> Homicide includes crimes such as first and second degree murder, involuntary manslaughter, and vehicular homicide.</p>
<p><b>Rape:</b> Rape also includes statutory rape and sexual assault.</p>
